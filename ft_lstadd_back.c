#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*end;

	if (new)
	{
		end = ft_lstlast(*lst);
		if (end)
			end->next = new;
		else
			*lst = new;
	}
}
